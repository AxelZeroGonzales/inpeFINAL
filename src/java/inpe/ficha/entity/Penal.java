package inpe.ficha.entity;
public class Penal {
    private int codPenal;
    private String nom_penal;
    private String c_regionPenal;

    public int getCodPenal() {
        return codPenal;
    }

    public void setCodPenal(int codPenal) {
        this.codPenal = codPenal;
    }

    public String getNom_penal() {
        return nom_penal;
    }

    public void setNom_penal(String nom_penal) {
        this.nom_penal = nom_penal;
    }

    public String getC_regionPenal() {
        return c_regionPenal;
    }

    public void setC_regionPenal(String c_regionPenal) {
        this.c_regionPenal = c_regionPenal;
    }
    
}
